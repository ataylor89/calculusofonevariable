import turtle

turtle.shape('turtle')

turtle.penup()
turtle.left(180)
turtle.forward(50)
turtle.right(180)
turtle.pendown()

turtle.color("blue")
turtle.circle(100)

turtle.penup()
turtle.forward(100)
turtle.pendown()

turtle.circle(100)

turtle.penup()
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(50)
turtle.pendown()

turtle.circle(100)

turtle.done()
